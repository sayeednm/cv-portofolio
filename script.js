// ===== ACTIVE NAV LINK =====
const sections = document.querySelectorAll('section[id]');
window.addEventListener('scroll', () => {
  const scrollY = window.scrollY + 100;
  sections.forEach(section => {
    const link = document.querySelector(`.nav-links a[href="#${section.id}"]`);
    if (!link) return;
    const top = section.offsetTop, height = section.offsetHeight;
    link.style.color = scrollY >= top && scrollY < top + height ? 'var(--cyan)' : '';
  });
});

// ===== PROJECT PREVIEW MODAL =====
function openProjectModal(url, title) {
  const modal = document.getElementById('projModal');
  const frame = document.getElementById('projModalFrame');
  const fallback = document.getElementById('projModalFallback');
  const modalTitle = document.getElementById('projModalTitle');
  const openLink = document.getElementById('projModalOpen');
  const fallbackLink = document.getElementById('projModalFallbackLink');

  modalTitle.textContent = title;
  openLink.href = url;
  fallbackLink.href = url;
  frame.src = url;
  fallback.classList.remove('show');

  // detect if iframe blocked (X-Frame-Options)
  frame.onerror = () => { fallback.classList.add('show'); };
  const timer = setTimeout(() => {
    try {
      // if contentDocument is null, it's blocked
      if (!frame.contentDocument && !frame.contentWindow?.location?.href) {
        fallback.classList.add('show');
      }
    } catch(e) {
      // cross-origin blocked — show fallback
      fallback.classList.add('show');
    }
  }, 4000);
  frame._timer = timer;

  modal.classList.add('open');
  document.body.style.overflow = 'hidden';
}
function closeProjectModal() {
  const modal = document.getElementById('projModal');
  const frame = document.getElementById('projModalFrame');
  clearTimeout(frame._timer);
  frame.src = '';
  modal.classList.remove('open');
  document.body.style.overflow = '';
}
document.getElementById('projModal').addEventListener('click', function(e) {
  if (e.target === this) closeProjectModal();
});

// ===== DESIGN FILTER =====
document.querySelectorAll('.filter-btn').forEach(btn => {
  btn.addEventListener('click', () => {
    document.querySelectorAll('.filter-btn').forEach(b => b.classList.remove('active'));
    btn.classList.add('active');
    const filter = btn.getAttribute('data-filter');
    document.querySelectorAll('.design-card').forEach(card => {
      const match = filter === 'all' || card.getAttribute('data-category') === filter;
      card.classList.toggle('hidden', !match);
    });
  });
});

// ===== LIGHTBOX =====
function openLightbox(btn) {
  const wrap = btn.closest('.design-img-wrap');
  const img = wrap.querySelector('img');
  const lb = document.getElementById('lightbox');
  const lbImg = document.getElementById('lightboxImg');
  if (img) {
    lbImg.src = img.src;
    lbImg.alt = img.alt || 'Design';
    lb.classList.add('open');
    document.body.style.overflow = 'hidden';
  }
}
function closeLightbox() {
  document.getElementById('lightbox').classList.remove('open');
  document.body.style.overflow = '';
}
document.addEventListener('keydown', e => { if (e.key === 'Escape') closeLightbox(); });

// ===== CONTACT FORM =====
function handleSubmit(e) {
  e.preventDefault();
  const btn = e.target.querySelector('button[type="submit"]');
  const msg = currentLang === 'id' ? 'Pesan Terkirim!' : 'Message Sent!';
  btn.textContent = msg;
  btn.style.background = '#10b981';
  setTimeout(() => {
    btn.innerHTML = translations[currentLang]['contact.form.send'];
    btn.style.background = '';
    e.target.reset();
  }, 3000);
}
